def validate_description(description: str, model_id: str) -> tuple[bool, str]:
    """–í–∞–ª–∏–¥–∞—Ü–∏—è –æ–ø–∏—Å–∞–Ω–∏—è –ø–æ —Ç—Ä–µ–±–æ–≤–∞–Ω–∏—è–º."""
    if not description:
        return False, f"{model_id}: –ø—É—Å—Ç–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ"
    
    if len(description) > 220:
        return False, f"{model_id}: –æ–ø–∏—Å–∞–Ω–∏–µ –¥–ª–∏–Ω–Ω–µ–µ 220 —Å–∏–º–≤–æ–ª–æ–≤ ({len(description)})"
    
    if len(description) < 50:
        return False, f"{model_id}: –æ–ø–∏—Å–∞–Ω–∏–µ –∫–æ—Ä–æ—á–µ 50 —Å–∏–º–≤–æ–ª–æ–≤ ({len(description)})"
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –ø–∞—Ñ–æ—Å–Ω—ã–µ —Å–ª–æ–≤–∞
    bad_words = ["–ª—É—á—à–∏–π", "—Ç–æ–ø", "—Ä–µ–≤–æ–ª—é—Ü–∏—è", "–≥–∞—Ä–∞–Ω—Ç–∏—è", "–Ω–µ–ø—Ä–µ–≤–∑–æ–π–¥—ë–Ω–Ω—ã–π", "–∏–¥–µ–∞–ª—å–Ω—ã–π"]
    for word in bad_words:
        if word in description.lower():
            return False, f"{model_id}: –Ω–∞–π–¥–µ–Ω–æ –ø–∞—Ñ–æ—Å–Ω–æ–µ —Å–ª–æ–≤–æ '{word}'"
    
    return True, ""


def add_descriptions_to_yaml(yaml_path: Path) -> dict:
    """–î–æ–±–∞–≤–ª—è–µ—Ç –æ–ø–∏—Å–∞–Ω–∏—è –≤ YAML —Ñ–∞–π–ª."""
    with open(yaml_path, 'r', encoding='utf-8') as f:
        data = yaml.safe_load(f)
    
    stats = {
        'total': len(data['models']),
        'added': 0,
        'existing': 0,
        'missing': 0,
        'errors': []
    }
    
    for model in data['models']:
        model_id = model['id']
        
        if model.get('description_ru'):
            stats['existing'] += 1
            # –í–∞–ª–∏–¥–∏—Ä—É–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–µ
            valid, error = validate_description(model['description_ru'], model_id)
            if not valid:
                stats['errors'].append(error)
            continue
        
        if model_id in MODEL_DESCRIPTIONS:
            description = MODEL_DESCRIPTIONS[model_id]
            
            # –í–∞–ª–∏–¥–∞—Ü–∏—è
            valid, error = validate_description(description, model_id)
            if not valid:
                stats['errors'].append(error)
                continue
            
            model['description_ru'] = description
            stats['added'] += 1
        else:
            stats['missing'] += 1
            stats['errors'].append(f"‚ùå {model_id}: –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ —Å–ª–æ–≤–∞—Ä–µ –æ–ø–∏—Å–∞–Ω–∏–π")
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –æ–±—Ä–∞—Ç–Ω–æ
    with open(yaml_path, 'w', encoding='utf-8') as f:
        yaml.dump(data, f, allow_unicode=True, sort_keys=False, default_flow_style=False)
    
    return stats


